export interface TierCriteria { wager: number; netLoss: number; deposits: number; activeDays: number; }
export interface TierBenefits {
  cashbackPct: number;
  rewardMultiplier: number;
  xpMultiplier: number;
  withdrawalPerk: string;
  vipManager: boolean;
  perks: string[];
}
export interface DraftTier {
  id: string;
  name: string;
  color: TierColor;
  entry: TierCriteria;
  maintain: TierCriteria;
  benefits: TierBenefits;
  estPlayers: number; // projected population — feeds liability preview
}

export const BONUS_PERKS = ['Birthday bonus', 'Weekly free spins', 'Priority support', 'Exclusive tournaments', 'Reload bonuses', 'Cashback insurance'];
export const WITHDRAWAL_PERKS = ['Standard', 'Priority (24h)', 'Instant', 'Dedicated VIP payout'];
export const CRITERIA_COLS: { key: keyof TierCriteria; label: string; prefix?: string; suffix?: string }[] = [
  { key: 'wager', label: 'Wager volume', prefix: '€' },
  { key: 'netLoss', label: 'Net loss', prefix: '€' },
  { key: 'deposits', label: 'Deposits', prefix: '€' },
  { key: 'activeDays', label: 'Active days', suffix: 'd' },
];

let seq = 0;
const uid = (p: string) => { seq += 1; return `${p}-${Date.now()}-${seq}`; };

const TIER_TEMPLATE: { name: string; color: TierColor }[] = [
  { name: 'Bronze', color: 'bronze' },
  { name: 'Silver', color: 'silver' },
  { name: 'Gold', color: 'gold' },
  { name: 'Platinum', color: 'platinum' },
  { name: 'Diamond', color: 'diamond' },
  { name: 'VIP', color: 'diamond' },
];
const DEFAULT_CRIT: TierCriteria[] = [
  { wager: 0, netLoss: 0, deposits: 0, activeDays: 0 },
  { wager: 2500, netLoss: 250, deposits: 500, activeDays: 5 },
  { wager: 10000, netLoss: 1000, deposits: 2000, activeDays: 10 },
  { wager: 40000, netLoss: 4000, deposits: 8000, activeDays: 15 },
  { wager: 120000, netLoss: 12000, deposits: 25000, activeDays: 20 },
  { wager: 400000, netLoss: 40000, deposits: 80000, activeDays: 25 },
];
const DEFAULT_CASHBACK = [5, 8, 10, 12, 15, 20];
const DEFAULT_MULT = [1, 1.25, 1.5, 2, 2.5, 3];
const DEFAULT_PLAYERS = [5200, 2800, 1300, 500, 160, 40];

export function makeTier(index: number): DraftTier {
  const i = Math.min(index, TIER_TEMPLATE.length - 1);
  const tpl = TIER_TEMPLATE[i];
  const crit = DEFAULT_CRIT[i];
  const maint = { ...crit, wager: Math.round(crit.wager * 0.6), netLoss: Math.round(crit.netLoss * 0.6) };
  return {
    id: uid('tier'), name: tpl.name, color: tpl.color,
    entry: { ...crit }, maintain: maint,
    benefits: { cashbackPct: DEFAULT_CASHBACK[i], rewardMultiplier: DEFAULT_MULT[i], xpMultiplier: DEFAULT_MULT[i], withdrawalPerk: i >= 3 ? 'Priority (24h)' : 'Standard', vipManager: i >= 4, perks: i >= 2 ? ['Birthday bonus'] : [] },
    estPlayers: DEFAULT_PLAYERS[i],
  };
}

// ─────────────────────────────────────────────────────────────
// Rules (WHEN / IF / THEN)
// ─────────────────────────────────────────────────────────────
export interface RuleCondition { id: string; field: string; op: string; value: string; }
export interface ProgramRule { id: string; when: string; conditions: RuleCondition[]; thenAction: string; thenValue: string; }
export const RULE_WHEN = ['Player deposits', 'Wager settled', 'Period reset', 'Tier maintenance check', 'Net loss recorded'];
export const RULE_FIELDS = ['Wager volume (period)', 'Net loss (period)', 'Deposits (period)', 'Active days', 'Current tier'];
export const RULE_OPS = ['at least', 'at most', 'equals', 'below'];
export const RULE_ACTIONS = ['Promote one tier', 'Demote one tier', 'Hold current tier', 'Grant bonus credit', 'Add loyalty points'];

export function newCondition(): RuleCondition { return { id: uid('cond'), field: RULE_FIELDS[0], op: 'at least', value: '' }; }
export function newRule(): ProgramRule { return { id: uid('rule'), when: RULE_WHEN[1], conditions: [newCondition()], thenAction: RULE_ACTIONS[0], thenValue: '' }; }

const ACTIONS_NEED_VALUE = new Set(['Grant bonus credit', 'Add loyalty points']);
export function ruleErrors(r: ProgramRule): string[] {
  const e: string[] = [];
  if (!r.when) e.push('Select a trigger event');
  r.conditions.forEach((c, i) => { if (!c.value.trim()) e.push(`Condition ${i + 1} needs a value`); });
  if (ACTIONS_NEED_VALUE.has(r.thenAction) && !r.thenValue.trim()) e.push(`${r.thenAction} needs an amount`);
  return e;
}
export function ruleSummary(r: ProgramRule): string {
  const conds = r.conditions.filter((c) => c.value.trim()).map((c) => `${c.field} ${c.op} ${c.value}`).join(' and ');
  const then = r.thenAction + (r.thenValue.trim() ? ` (${r.thenValue})` : '');
  return `${r.when || 'When…'}${conds ? `, if ${conds}` : ''} → ${then}`;
}

